    public void listTab(){
            DefaultTableModel modTab = (DefaultTableModel)tbFormCli.getModel(); //Reflexividade
            int posLin = 0;
            modTab.setRowCount(posLin);//Reflexividade

            for(Cliente clit : geCli.getBdCli()){
                modTab.insertRow(posLin, new Object[]{clit.getNome(),
                    clit.getCpf(),
                    clit.getTelefone(),
                    clit.getGasto()
                });//Reflexividade
                posLin++;
            }
        }
