    public void pesquisar(){
        atend1 = new Atendente();
        try{
            atend1.setCpf(Integer.parseInt(cxCpfAtend.getText()));//Reflexividade
            
            atend1 = gerAt.consAtendente(atend1);//Reflexividade
            
            if(atend1 != null){
                JOptionPane.showMessageDialog(
                        null,
                        "Atendente encontrado com sucesso!\nConfira a tabela",
                        "Consulta de Atendente",
                        JOptionPane.INFORMATION_MESSAGE
                        
                );//Reflexividade
                
                DefaultTableModel modTab = (DefaultTableModel)tbAtend.getModel(); //Reflexividade
                int posLin = 0;
                modTab.setRowCount(posLin);//Reflexividade

                for(Atendente aten : gerAt.getBdAtend()){
                    if (atend1.getCpf() == aten.getCpf()){
                        modTab.insertRow(posLin, new Object[]{aten.getNome(),
                            aten.getCpf(), aten.getCodAtendente(), 
                            aten.getEspecialidade()
                        });//Reflexividade
                        posLin++;
                    }
                }
                
                
            }
            else{
                JOptionPane.showMessageDialog(
                        null,
                        "Cpf nÃ£o encontrado!",
                        "Consulta de Atendente",
                        JOptionPane.QUESTION_MESSAGE
                );//Reflexividade
                cxCodAtend.setText("");//Reflexividade
                cxCodAtend.requestFocus();//Reflexividade
            }
            
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(
                    null,
                    "O cpf deve ser um nÃºmero inteiro!",
                    "Consulta de Atendente",
                    JOptionPane.ERROR_MESSAGE
            );//Reflexividade
            cxCpfAtend.setText("");//Reflexividade
            cxCpfAtend.requestFocus();//Reflexividade
        }
    }
    
